    private boolean validateInput() {
      userId=txtUserId.getText();
      char[] pwd =txtPassword.getPassword();
      char[] npwd=txtNewPassword.getPassword();
      if(userId.isEmpty()||pwd.length==0||npwd.length==0)
          return false;
      else
      {
          password=String.valueOf(pwd);
          newPassword=String.valueOf(npwd);
          return true;
      }
    }

    private void clearAll() {
        txtUserId.setText("");
        txtPassword.setText("");
        txtNewPassword.setText("");
    }
