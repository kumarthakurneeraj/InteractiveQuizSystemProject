    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed

        if(validateInput()==false)
        {
            JOptionPane.showMessageDialog(null, "Please fill all input fields", "Cannot add question", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String optionName=options.get(correctOption);
        Question obj=new Question(newExam.getExamId(),qno,newExam.getLanguage(),option1,option2,option3,option4,optionName,question);
        qStore.addQuestion(obj);
        clearAll();
        lblTitle.setText(newExam.getTotalQuestion()-qno+" QUESTIONS REMAINING");
        qno++;
        if(qno>newExam.getTotalQuestion())
        {
            disableAll();
            JOptionPane.showMessageDialog(null, "Your question set has been successfully created.\nPress the Done button to add it to database", "EXAM ADDED!", JOptionPane.INFORMATION_MESSAGE);
            
        }
        else
            lblQno.setText("Question no:"+qno);
        
    }//GEN-LAST:event_btnNextActionPerformed

