    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        if(qno<=newExam.getTotalQuestion())
        {
            int remainingQuestions=newExam.getTotalQuestion()-qno+1;
            JOptionPane.showMessageDialog(null, "You still have "+remainingQuestions+" left","Cannot Add Exam!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            ExamDao.addExam(newExam);
            QuestionDao.addQuestions(qStore);
            JOptionPane.showMessageDialog(null,"Your question set has been successfully added to your database","Exam Added!",JOptionPane.INFORMATION_MESSAGE);
            AdminOptionFrame adminOption=new AdminOptionFrame();
            adminOption.setVisible(true);
            this.dispose();
        }
        catch(SQLException ex)
        {
            
        JOptionPane.showMessageDialog(null,"Error while connecting to DB!","EXCEPTION!",JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDoneActionPerformed

