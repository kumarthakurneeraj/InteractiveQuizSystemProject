    private void btnUpdatePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePasswordActionPerformed
        // TODO add your handling code here:
        boolean ans=validateInput();
        if(ans==false)
        {
            JOptionPane.showMessageDialog(null, "Please fill all the field!", "ERROR!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            User user=new User();
            user.setUserName(userId);
            user.setPassword(newPassword);
            user.setUserType("Student");
            boolean result=UserDao.changePassword(user, password);
            if(result)
            {
                JOptionPane.showMessageDialog(null, "Password changed successfully!", "SUCCESS!", JOptionPane.INFORMATION_MESSAGE);
                clearAll();
            }

        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Please enter valid userid and password!", "EXCEPTION!,ERROR!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdatePasswordActionPerformed

